#Вивести на екран «прямокутник» розміру N на M,
# утворений з двох видів символів.
# Контур прямокутника повинен складатися з одного символу, а "заливка" - з іншого.


N = 5
M = 8


for i in range(N):
    for j in range(M):
        if(i == 0 or i == N - 1 or j == 0 or j == M - 1):
            print('N', end = '  ')
        else:
            print('m', end = '  ')
    print()
