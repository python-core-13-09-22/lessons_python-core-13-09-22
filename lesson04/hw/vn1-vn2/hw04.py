# Задано три довільних числа. Визначити, чи можна побудувати
# трикутник з такими довжинами сторін; Якщо так, то видрукувати його
# периметр та площу.
a = int(input("Введіть сторону трикутника a: "))
b = int(input("Введіть сторону трикутника b: "))
c = int(input("Введіть сторону трикутника c: "))
l = [a, b, c]
l.sort()
if l[2] < l[0] + l[1]:
    p = a + b + c
    s = math.sqrt(p/2*(p/2-a)*(p/2-b)*(p/2-c))
    print(f"Периметр трикутника = {p}, площа трикутника = {s}")
else:
    print("Неможливо побудувати трикутник")

# Нехай `k`- ціле від `1` до `365`. Присвоїти цілій змінній n значення
# (понеділок, вівторок, …, суботу чи неділю) залежно від того , на який
# день тижня припадає `k`-й день не високосного року, в якому 1 січня -
# понеділок.
k = int(input("Введіть число від 1 до 365: "))
week = ["Понеділок", "Вівторок", "Середа", "Четвер", "П'ятниця", "Субота", "Неділя"]
i = 0
while i < k:
    for n in week:
        i += 1
print(f"{k} день не високосного року це {n}")
