d = int(input("Enter a day: "))
x = datetime.datetime.now()
if int(x.strftime("%Y")) == y and int(x.strftime("%m")) == m and int(x.strftime("%d")) == d:
    print("It's a correct date!")
else:
    print("It's not a correct date!!")

